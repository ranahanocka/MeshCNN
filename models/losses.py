def postprocess(true, pred):
    num_classses = pred.shape[1]
    true = true.view(-1)
    pred = pred.view(num_classses, -1)
    not_padding = true != -1
    true = true[not_padding]
    pred = pred[:, not_padding]
    true = true.view(1, 1, -1, 1)
    pred = pred.view(1, num_classses, -1, 1)
    return true, pred